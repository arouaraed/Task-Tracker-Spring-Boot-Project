package com.raed.tasks.sevices.impl;

import com.raed.tasks.domain.entities.TaskList;
import com.raed.tasks.repositories.TaskListRepository;
import com.raed.tasks.sevices.TaskListService;
import jakarta.transaction.Transactional;
import org.springframework.stereotype.Service;

import java.time.LocalDateTime;
import java.util.List;
import java.util.Objects;
import java.util.Optional;
import java.util.UUID;

@Service
public class TaskListServiceImpl implements TaskListService {

    private final TaskListRepository taskListRepository;
    public TaskListServiceImpl(TaskListRepository taskListRepository) {
        this.taskListRepository = taskListRepository;
    }
    @Override
    public List<TaskList> ListTaskLists() {
        return taskListRepository.findAll();
    }
    @Transactional
    @Override
    public TaskList createTaskList(TaskList taskList) {
        if(null != taskList.getId()){
            throw new IllegalArgumentException("task list already have an id");

        }
        if(null == taskList.getTitle()|| taskList.getTitle().isBlank()){
            throw new IllegalArgumentException("task list title must be present");

        }
        LocalDateTime now = LocalDateTime.now();
        return taskListRepository.save(new TaskList(
            null ,
                taskList.getTitle(),
                taskList.getDescription(),
                now,
                null,
                now

        ));
    }

    @Override
    public Optional<TaskList> getTaskListById(UUID id) {
        return taskListRepository.findById(id);
    }
    @Transactional
    @Override
    public TaskList updateTaskList(UUID taskListId, TaskList taskList) {
        if(null == taskList.getId()){
            throw new IllegalArgumentException("task list already have an id");

        }
        if(!Objects.equals(taskList.getId(), taskListId)){
            throw new IllegalArgumentException("Attempting to change task list id, this is not permitted");
        }
        TaskList existingTaskList= taskListRepository.findById(taskListId).orElseThrow(() -> new IllegalArgumentException("task list not found"));
        existingTaskList.setTitle(taskList.getTitle());
        existingTaskList.setDescription(taskList.getDescription());
        existingTaskList.setUpdated(LocalDateTime.now());
        return taskListRepository.save(existingTaskList);

    }

    @Override
    public void deleteTaskList(UUID taskListId) {
        taskListRepository.deleteById(taskListId);

    }
}
